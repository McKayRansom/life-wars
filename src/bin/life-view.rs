use life_io::{life::from_plaintext, viewer::LifeViewer};
use macroquad::{
    texture::set_default_filter_mode,
    window::{self, next_frame},
};

fn window_conf() -> window::Conf {
    window::Conf {
        fullscreen: false,
        // high-dpi seems to change the zoom on webassembly??
        high_dpi: true,
        // icon: Some(Icon {
        //     small: include_bytes!("../icons/16x16.rgba").to_owned(),
        //     medium: include_bytes!("../icons/32x32.rgba").to_owned(),
        //     big: include_bytes!("../icons/64x64.rgba").to_owned(),
        // }),
        // platform: miniquad::conf::Platform {
        //     linux_backend: miniquad::conf::LinuxBackend::WaylandWithX11Fallback,
        //     ..Default::default()
        // },
        window_height: 720,
        window_resizable: true,
        window_title: String::from("Life-IO"),
        window_width: 1280,
        ..Default::default()
    }
}

#[macroquad::main(window_conf)]
async fn main() {
    set_default_filter_mode(window::miniquad::FilterMode::Nearest);

    let mut viewer = LifeViewer::new_fit_to_screen(Box::new(from_plaintext(
        "!Name: 
        .................................
        .................................
        .................................
        ....................OO...........
        ....................OO...........
        .................................
        .................................
        .................................
        .................................
        .................................
        .................OO..............
        ...OO...........OOOO.............
        ...OO.......O.OO.OO.O............
        ...........O..OO.................
        ..........OOO..OO..OO............
        ..........OOO..O.OOOO............
        ...........O..O...O..O...........
        ............OOOO.O..OOO..........
        ............OO..OO..OOO..........
        .................OO..O...........
        ............O.OO.OO.O.......OO...
        .............OOOO...........OO...
        ..............OO.................
        .................................
        .................................
        .................................
        .................................
        .................................
        ...........OO....................
        ...........OO....................
        .................................
        .................................
        .................................",
        None,
        None,
    )));

    loop {
        viewer.update();
        viewer.handle_input();
        viewer.draw();
        next_frame().await;
    }
}
